package bitcamp.java100.ch06.ex4.test7;

public class Calculator {
    
    int result;
    
    void plus(int value) {
        this.result += value;
    }
    
    void minus(int value) {
        this.result -= value;
    }
    
    void multiple(int value) {
        this.result *= value;
    }
    
    void devide(int value) {
        this.result /= value;
    }
}
