package bitcamp.java100.ch06.ex4.test5;

public class Calculator {
    
    static int result;
    
    static void plus(int value) {
        result += value;
    }
    
    static void minus(int value) {
        result -= value;
    }
    
    static void multiple(int value) {
        result *= value;
    }
    
    static void devide(int value) {
        result /= value;
    }
}
